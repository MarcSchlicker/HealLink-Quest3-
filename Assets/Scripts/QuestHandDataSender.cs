    private bool ResolveTargetEndPoint()
    {
        if (Time.unscaledTime < nextTargetResolveTime)
        {
            return targetEndPoint != null;
        }

        nextTargetResolveTime = Time.unscaledTime + Mathf.Max(0.1f, targetResolveRetrySeconds);

        string host = GetTargetHost();
        if (string.IsNullOrEmpty(host))
        {
            if (!warnedAboutMissingHost)
            {
                warnedAboutMissingHost = true;
                Debug.LogWarning("QuestHandDataSender has no target IP. Set targetHost or fill run_once.host.");
            }

            return false;
        }

        if (targetEndPoint != null && host == resolvedHost && targetPort == resolvedPort)
        {
            return true;
        }

        try
        {
            IPAddress address;
            if (!IPAddress.TryParse(host, out address))
            {
                if (!allowDnsHostResolution)
                {
                    if (!warnedAboutMissingHost)
                    {
                        warnedAboutMissingHost = true;
                        Debug.LogWarning("QuestHandDataSender targetHost is not an IP address. Use a direct IPv4 address or enable allowDnsHostResolution.");
                    }

                    return false;
                }

                IPAddress[] addresses = Dns.GetHostAddresses(host);
                address = PickAddress(addresses);
            }

            if (address == null)
            {
                Debug.LogWarning("QuestHandDataSender could not resolve target host: " + host);
                return false;
            }

            targetEndPoint = new IPEndPoint(address, targetPort);
            resolvedHost = host;
            resolvedPort = targetPort;
            warnedAboutMissingHost = false;
            WarnIfLinkLocalAddress(address);

            if (logConnectionChanges)
            {
                Debug.Log("QuestHandDataSender target is " + targetEndPoint);
            }

            return true;
        }
        catch (Exception e)
        {
            targetEndPoint = null;
            Debug.LogWarning("QuestHandDataSender could not resolve target host '" + host + "': " + e.Message);
            return false;
        }
    }

    private string GetTargetHost()
    {
        string explicitTargetHost = TrimToNull(targetHost);
        if (!string.IsNullOrEmpty(explicitTargetHost))
        {
            return explicitTargetHost;
        }

        if (useLocalhostInEditor && Application.isEditor)
        {
            string editorHost = TrimToNull(editorTargetHost);
            if (!string.IsNullOrEmpty(editorHost))
            {
                return editorHost;
            }
        }

        if (useRunOnceHost)
        {
            string runOnceStaticHost = TrimToNull(run_once.host_address);
            if (!string.IsNullOrEmpty(runOnceStaticHost))
            {
                return runOnceStaticHost;
            }

            run_once runOnce = FindObjectOfType<run_once>();
            string runOnceSceneHost = runOnce != null ? TrimToNull(runOnce.host) : null;
            if (!string.IsNullOrEmpty(runOnceSceneHost))
            {
                return runOnceSceneHost;
            }
        }

        return null;
    }

    private static string TrimToNull(string value)
    {
        if (string.IsNullOrEmpty(value))
        {
            return null;
        }

        string trimmed = value.Trim();
        return string.IsNullOrEmpty(trimmed) ? null : trimmed;
    }

    private static IPAddress PickAddress(IPAddress[] addresses)
    {
        if (addresses == null || addresses.Length == 0)
        {
            return null;
        }

        for (int i = 0; i < addresses.Length; i++)
        {
            if (addresses[i] != null && addresses[i].AddressFamily == AddressFamily.InterNetwork)
            {
                return addresses[i];
            }
        }

        return addresses[0];
    }

    private void WarnIfLinkLocalAddress(IPAddress address)
    {
        if (!warnAboutLinkLocalTarget || warnedAboutLinkLocalTarget || address == null)
        {
            return;
        }

        byte[] bytes = address.GetAddressBytes();
        bool isIpv4LinkLocal = address.AddressFamily == AddressFamily.InterNetwork &&
                               bytes.Length == 4 &&
                               bytes[0] == 169 &&
                               bytes[1] == 254;

        if (!isIpv4LinkLocal)
        {
            return;
        }

        warnedAboutLinkLocalTarget = true;
        Debug.LogWarning("QuestHandDataSender target uses a 169.254.x.x link-local IP. " +
                         "That often only works for USB/direct connections. For Quest-to-HoloLens over Wi-Fi, use the HoloLens Wi-Fi IPv4 address.");
    }

