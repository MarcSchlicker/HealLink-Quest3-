    private void Start()
    {
        if (autoFindHands)
        {
            FindHandSources();
        }

        if (autoFindReferenceCamera)
        {
            FindReferenceCamera();
        }

        if (autoFindDepthProjectionSource)
        {
            FindDepthProjectionSource();
        }

        if (autoFindBillboardMarkerSource)
        {
            FindBillboardMarkerSource();
        }

    }

    private void OnDisable()
    {
        StopWebRtcAudio();
        HideLeftDepthHandPreview();
        StopSender();
        StopMicrophoneCapture();
        CloseAudioDiagnosticsWriter();
        CloseOutgoingAudioWavWriter();
    }

    private void OnDestroy()
    {
        StopWebRtcAudio();
        DestroyLeftDepthHandPreview();
        StopSender();
        StopMicrophoneCapture();
        CloseAudioDiagnosticsWriter();
        CloseOutgoingAudioWavWriter();

        if (questStrokeMaterial != null)
        {
            Destroy(questStrokeMaterial);
            questStrokeMaterial = null;
        }
    }

    private void Update()
    {
        if (!isSending)
        {
            return;
        }

        if (autoFindHands && Time.unscaledTime >= nextFindTime)
        {
            nextFindTime = Time.unscaledTime + 1f;
            if (!HasBothSkeletons())
            {
                FindHandSources();
            }
        }

        if (autoFindReferenceCamera && referenceCamera == null && Time.unscaledTime >= nextReferenceFindTime)
        {
            nextReferenceFindTime = Time.unscaledTime + 1f;
            FindReferenceCamera();
        }

        if (autoFindDepthProjectionSource && depthProjectionSource == null && Time.unscaledTime >= nextDepthProjectionFindTime)
        {
            nextDepthProjectionFindTime = Time.unscaledTime + 1f;
            FindDepthProjectionSource();
        }

        if (autoFindBillboardMarkerSource && billboardMarkerSource == null && Time.unscaledTime >= nextBillboardMarkerFindTime)
        {
            nextBillboardMarkerFindTime = Time.unscaledTime + 1f;
            FindBillboardMarkerSource();
        }

        UpdateLeftPinchStroke();

        if (sendAudioAsSeparatePacket && !useWebRtcAudio)
        {
            SendDueAudioFrames();
        }

        float interval = sendRateHz > 0f ? 1f / sendRateHz : 0f;
        if (interval > 0f && Time.unscaledTime < nextSendTime)
        {
            return;
        }

        nextSendTime = Time.unscaledTime + interval;
        SendCurrentFrame();
    }

    private void LateUpdate()
    {
        UpdateLeftDepthHandPreview();
    }

    public void StartSender()
    {
        startSenderCoroutine = null;
        if (isSending)
        {
            return;
        }

        try
        {
            udpClient = new UdpClient();
            isSending = true;
            warnedAboutMissingHost = false;
            warnedAboutMissingHands = false;
            warnedAboutLinkLocalTarget = false;
            warnedAboutMissingLeftPinchSource = false;
            warnedAboutMissingStrokePoint = false;
            microphonePermissionDenied = false;
            microphoneStartFailed = false;
            earliestMicrophoneStartTime = Time.unscaledTime + Mathf.Max(0f, microphoneStartDelaySeconds);
            nextTargetResolveTime = 0f;
        }
        catch (Exception e)
        {
            isSending = false;
            Debug.LogError("QuestHandDataSender could not start UDP sender: " + e.Message);
        }
    }

    private void StartSenderDelayed()
    {
        if (startSenderCoroutine != null)
        {
            StopCoroutine(startSenderCoroutine);
        }

        startSenderCoroutine = StartCoroutine(StartSenderAfterDelay());
    }

    private IEnumerator StartSenderAfterDelay()
    {
        float delay = Mathf.Max(0f, networkStartDelaySeconds);
        if (delay > 0f)
        {
            yield return new WaitForSeconds(delay);
        }

        StartSender();
    }

    public void StopSender()
    {
        if (startSenderCoroutine != null)
        {
            StopCoroutine(startSenderCoroutine);
            startSenderCoroutine = null;
        }

        FinishActiveStrokePreview();
        leftPinchWasActive = false;
        activeStrokeId = null;
        ResetRightHandBillboardAlignment();
        currentQuestStrokePoints.Clear();
        strokeEventQueue.Clear();

        isSending = false;
        targetEndPoint = null;
        resolvedHost = null;
        resolvedPort = 0;

        if (udpClient != null)
        {
            udpClient.Close();
            udpClient = null;
        }
    }

    private void SendCurrentFrame()
    {
        if (udpClient == null || !ResolveTargetEndPoint())
        {
            return;
        }

        AudioFrame audioFrame = sendAudioAsSeparatePacket || useWebRtcAudio
            ? null
            : TryBuildAudioFrame();
        BuildPacket(audioFrame);
        bool hasHands = packet.hands != null && packet.hands.Length > 0;
        bool hasStrokes = packet.strokeEvents != null && packet.strokeEvents.Length > 0;
        bool hasAudio = packet.audioFrame != null && !string.IsNullOrEmpty(packet.audioFrame.samplesBase64);
        if (!hasHands && !hasStrokes && !hasAudio)
        {
            if (audioFrame == null && !warnedAboutMissingHands)
            {
                warnedAboutMissingHands = true;
                Debug.LogWarning("QuestHandDataSender found no Meta hand sources, stroke events, or audio samples to send.");
            }
        }
        else if (SendPacket(packet))
        {
            strokeEventQueue.Clear();
        }
    }

    private void SendDueAudioFrames()
    {
        if (!sendQuestMicrophoneAudio || udpClient == null || !ResolveTargetEndPoint())
        {
            return;
        }

        int maxPackets = Mathf.Clamp(maximumAudioPacketsPerUpdate, 1, 8);
        for (int i = 0; i < maxPackets; i++)
        {
            AudioFrame audioFrame = TryBuildAudioFrame();
            if (audioFrame == null)
            {
                return;
            }

            SendAudioPacket(audioFrame);
        }
    }

    private bool SendPacket(HandFramePacket packetToSend)
    {
        try
        {
            string json = JsonUtility.ToJson(packetToSend);
            byte[] bytes = Encoding.UTF8.GetBytes(json);
            udpClient.Send(bytes, bytes.Length, targetEndPoint);

            if (logPackets)
            {
                Debug.Log("QuestHandDataSender sent " + bytes.Length + " bytes to " + targetEndPoint + " (" + packetToSend.packetType + ")");
            }

            return true;
        }
        catch (Exception e)
        {
            Debug.LogWarning("QuestHandDataSender UDP send failed: " + e.Message);
            targetEndPoint = null;
            return false;
        }
    }

    private void SendAudioPacket(AudioFrame audioFrame)
    {
        audioOnlyPacket.packetType = "questAudio";
        audioOnlyPacket.poseSpace = "";
        audioOnlyPacket.hands = null;
        audioOnlyPacket.strokeEvents = null;
        audioOnlyPacket.audioFrame = audioFrame;
        SendPacket(audioOnlyPacket);
        audioOnlyPacket.audioFrame = null;
    }

    private void BuildPacket(AudioFrame audioFrame)
    {
        frameBuffer.Clear();
        packet.packetType = "questFrame";
        packet.poseSpace = sendCameraRelativePoses && referenceCamera != null ? "CameraLocal" : "World";

        AddHandFrame("Left", leftHand, leftSkeleton);
        AddHandFrame("Right", rightHand, rightSkeleton);

        packet.hands = frameBuffer.ToArray();
        packet.strokeEvents = strokeEventQueue.Count > 0 ? strokeEventQueue.ToArray() : null;
        packet.audioFrame = audioFrame;
    }

    private void AddHandFrame(string handedness, OVRHand hand, OVRSkeleton skeleton)
    {
        bool isTracked = IsHandTracked(hand, skeleton);
        bool isRightHand = string.Equals(handedness, "Right", StringComparison.OrdinalIgnoreCase);
        if (!isTracked && isRightHand)
        {
            ResetRightHandBillboardAlignment();
        }

        if (!isTracked && !sendUntrackedFrames)
        {
            return;
        }

        HandFrame frame = new HandFrame();
        frame.handedness = handedness;
        frame.isTracked = isTracked;
        frame.wrist = PoseData.Invalid;
        frame.joints = new JointPose[0];

        if (isTracked && skeleton != null && skeleton.Bones != null)
        {
            jointBuffer.Clear();
            Vector3 streamedWorldOffset = isRightHand
                ? GetRightHandBillboardOffset(skeleton)
                : Vector3.zero;

            for (int i = 0; i < skeleton.Bones.Count; i++)
            {
                OVRBone bone = skeleton.Bones[i];
                if (bone == null || bone.Transform == null)
                {
                    continue;
                }

                string jointName = bone.Id.ToString();
                PoseData pose = PoseData.FromTransform(
                    bone.Transform,
                    sendCameraRelativePoses ? referenceCamera : null,
                    positionScale,
                    streamedWorldOffset);

                if (IsWristBone(bone.Id) && !frame.wrist.isValid)
                {
                    frame.wrist = pose;
                }

                JointPose joint = new JointPose();
                joint.name = jointName;
                joint.pose = pose;
                jointBuffer.Add(joint);
            }

            frame.joints = jointBuffer.ToArray();
        }

        frameBuffer.Add(frame);
    }

    private void UpdateLeftPinchStroke()
    {
        if (!enableLeftPinchStroke || !IsLeftPinchActive())
        {
            hasCurrentPinchDepthPoint = false;
            EndActiveLeftPinchStroke();
            return;
        }

        Vector3 pinchPoint;
        currentStrokePointSkippedForDepth = false;
        if (!TryGetCurrentStrokeWorldPoint(out pinchPoint))
        {
            hasCurrentPinchDepthPoint = false;
            if (!currentStrokePointSkippedForDepth)
            {
                WarnAboutMissingStrokePoint();
            }
            return;
        }

        hasCurrentPinchDepthPoint = true;
        currentPinchDepthPoint = pinchPoint;

        if (!leftPinchWasActive)
        {
            BeginLeftPinchStroke(pinchPoint);
        }
        else
        {
            AppendLeftPinchStrokePoint(pinchPoint);
        }

        leftPinchWasActive = true;
    }

    private bool IsLeftPinchActive()
    {
        if (!useMetaHandTrackingBlockPinch)
        {
            return false;
        }

        if (leftHand == null || leftSkeleton == null)
        {
            if (autoFindHands)
            {
                FindHandSources();
            }
        }

        if (leftHand == null)
        {
            WarnAboutMissingLeftPinchSource();
            return false;
        }

        if (!leftHand.IsTracked || !leftHand.IsDataValid)
        {
            return false;
        }

        if (requireHighConfidence && !leftHand.IsDataHighConfidence)
        {
            return false;
        }

        warnedAboutMissingLeftPinchSource = false;
        return leftHand.GetFingerIsPinching(OVRHand.HandFinger.Index);
    }

    private bool TryGetCurrentStrokeWorldPoint(out Vector3 worldPoint)
    {
        Vector3 sourcePoint;
        if (strokePointOverride != null)
        {
            sourcePoint = strokePointOverride.position;
            return TryResolveStrokePoint(sourcePoint, out worldPoint);
        }

        Vector3 thumbTip;
        Vector3 indexTip;
        if (TryGetFingerTipPositions(leftSkeleton, out thumbTip, out indexTip))
        {
            sourcePoint = (thumbTip + indexTip) * 0.5f;
            return TryResolveStrokePoint(sourcePoint, out worldPoint);
        }

        if (leftHand != null && leftHand.IsPointerPoseValid)
        {
            Transform pointerPose = leftHand.PointerPose;
            if (pointerPose != null)
            {
                sourcePoint = pointerPose.position;
                return TryResolveStrokePoint(sourcePoint, out worldPoint);
            }
        }

        worldPoint = Vector3.zero;
        return false;
    }

    private bool TryResolveStrokePoint(Vector3 sourcePoint, out Vector3 worldPoint)
    {
        if (projectStrokeOntoDepth)
        {
            if (TryProjectStrokePointOntoDepth(sourcePoint, out worldPoint))
            {
                hasLastDepthProjectedStrokePoint = true;
                lastDepthProjectedStrokePoint = worldPoint;
                return true;
            }

            if (skipStrokePointsWithoutDepth)
            {
                currentStrokePointSkippedForDepth = true;
                worldPoint = Vector3.zero;
                return false;
            }

            if (leftPinchWasActive && hasLastDepthProjectedStrokePoint)
            {
                worldPoint = lastDepthProjectedStrokePoint;
                return IsFinite(worldPoint);
            }
        }

        worldPoint = sourcePoint;
        return IsFinite(worldPoint);
    }

    private bool TryProjectStrokePointOntoDepth(Vector3 sourcePoint, out Vector3 depthPoint)
    {
        depthPoint = Vector3.zero;

        if (depthProjectionSource == null && autoFindDepthProjectionSource)
        {
            if (Time.unscaledTime >= nextDepthProjectionFindTime)
            {
                nextDepthProjectionFindTime = Time.unscaledTime + 1f;
                FindDepthProjectionSource();
            }
        }

        if (depthProjectionSource == null)
        {
            WarnAboutMissingDepthProjectionSource();
            return false;
        }

        Transform projectionCamera = depthProjectionCamera != null ? depthProjectionCamera : referenceCamera;
        if (projectionCamera == null && autoFindReferenceCamera)
        {
            FindReferenceCamera();
            projectionCamera = referenceCamera;
        }

        if (projectionCamera == null)
        {
            WarnAboutMissingDepthProjectionSource();
            return false;
        }

        float depthMeters;
        float rawValue;
        Vector2 billboardUV;
        if (!depthProjectionSource.TryProjectWorldPointToDepth(
                sourcePoint,
                projectionCamera,
                out depthPoint,
                out depthMeters,
                out rawValue,
                out billboardUV))
        {
            return false;
        }

        if (applyBillboardCalibrationToStroke)
        {
            Vector2 centeredBillboardCoordinates = new Vector2(
                Mathf.Clamp(billboardUV.x * 2f - 1f, -1f, 1f),
                Mathf.Clamp(billboardUV.y * 2f - 1f, -1f, 1f));
            depthPoint += GetDepthScaledBillboardCorrection(
                projectionCamera,
                centeredBillboardCoordinates,
                depthMeters);
        }

        if (!IsFinite(depthPoint))
        {
            return false;
        }

        if (!IsProjectedStrokePointSafe(depthPoint, projectionCamera, out float forwardDistance))
        {
            return false;
        }

        hasLastDepthProjectedForwardDistance = true;
        lastDepthProjectedForwardDistance = forwardDistance;
        warnedAboutMissingDepthProjectionSource = false;
        return true;
    }

    private bool IsProjectedStrokePointSafe(Vector3 depthPoint, Transform projectionCamera, out float forwardDistance)
    {
        forwardDistance = 0f;

        if (projectionCamera == null || !IsFinite(depthPoint))
        {
            return false;
        }

        Vector3 cameraToPoint = depthPoint - projectionCamera.position;
        if (cameraToPoint.sqrMagnitude < 1e-8f)
        {
            return false;
        }

        Vector3 cameraForward = projectionCamera.forward;
        if (cameraForward.sqrMagnitude < 1e-8f)
        {
            return false;
        }

        forwardDistance = Vector3.Dot(cameraToPoint, cameraForward.normalized);
        if (!IsFinite(forwardDistance))
        {
            return false;
        }

        if (rejectStrokePointsNearCamera && forwardDistance < minimumStrokeDistanceFromCamera)
        {
            currentStrokePointSkippedForDepth = true;
            return false;
        }

        if (maximumStrokeHeadwardJump > 0f &&
            hasLastDepthProjectedForwardDistance &&
            forwardDistance < lastDepthProjectedForwardDistance - maximumStrokeHeadwardJump)
        {
            currentStrokePointSkippedForDepth = true;
            return false;
        }

        if (maximumStrokePointJump > 0f &&
            hasLastDepthProjectedStrokePoint &&
            Vector3.Distance(lastDepthProjectedStrokePoint, depthPoint) > maximumStrokePointJump)
        {
            currentStrokePointSkippedForDepth = true;
            return false;
        }

        return true;
    }

