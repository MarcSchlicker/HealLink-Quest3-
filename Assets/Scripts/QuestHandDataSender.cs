    private OVRSkeleton FindSkeleton(bool left)
    {
        string anchorName = left ? leftHandAnchorName : rightHandAnchorName;
        GameObject anchor = string.IsNullOrEmpty(anchorName) ? null : GameObject.Find(anchorName);
        if (anchor != null)
        {
            OVRSkeleton skeleton = anchor.GetComponentInChildren<OVRSkeleton>(true);
            if (skeleton != null)
            {
                return skeleton;
            }
        }

        OVRSkeleton[] skeletons = Resources.FindObjectsOfTypeAll<OVRSkeleton>();
        for (int i = 0; i < skeletons.Length; i++)
        {
            OVRSkeleton skeleton = skeletons[i];
            if (skeleton == null || !IsLoadedSceneObject(skeleton.gameObject))
            {
                continue;
            }

            OVRSkeleton.SkeletonType skeletonType = skeleton.GetSkeletonType();
            if (left && (skeletonType == OVRSkeleton.SkeletonType.HandLeft || skeletonType == OVRSkeleton.SkeletonType.XRHandLeft))
            {
                return skeleton;
            }

            if (!left && (skeletonType == OVRSkeleton.SkeletonType.HandRight || skeletonType == OVRSkeleton.SkeletonType.XRHandRight))
            {
                return skeleton;
            }
        }

        return null;
    }

