    private AudioFrame TryBuildAudioFrame()
    {
        if (!sendQuestMicrophoneAudio)
        {
            return null;
        }

        EnsureMicrophoneCapture();
        if (microphoneClip == null)
        {
            return null;
        }

        int packetRate = Mathf.Clamp(audioPacketRateHz, 1, 60);
        if (Time.unscaledTime < nextAudioPacketTime)
        {
            return null;
        }

        int microphonePosition = Microphone.GetPosition(activeMicrophoneDeviceName);
        if (microphonePosition < 0 || microphoneClip.samples <= 0)
        {
            return null;
        }

        int sourceChannels = Mathf.Max(1, microphoneClip.channels);
        int clipFrameCount = microphoneClip.samples;
        int captureSampleRate = Mathf.Max(1, microphoneClip.frequency);
        int outputSampleRate = GetNetworkAudioSampleRate(captureSampleRate);
        int outputChannels = GetNetworkAudioChannelCount(sourceChannels);
        int targetPacketFrameCount = Mathf.Max(1, Mathf.RoundToInt((float)captureSampleRate / packetRate));
        int availableFrameCount = GetAvailableMicrophoneFrames(microphonePosition, clipFrameCount);
        if (availableFrameCount < targetPacketFrameCount)
        {
            LogAudioStatus("waiting for microphone samples, position=" + microphonePosition +
                           ", availableFrames=" + availableFrameCount +
                           ", neededFrames=" + targetPacketFrameCount);
            return null;
        }

        int maxPacketFrameCount = Mathf.Max(
            targetPacketFrameCount,
            Mathf.RoundToInt(captureSampleRate * maximumAudioPacketDurationSeconds));
        int packetFrameCount = Mathf.Min(targetPacketFrameCount, maxPacketFrameCount);

        if (availableFrameCount > maxPacketFrameCount * 4)
        {
            microphoneReadPosition = microphonePosition - maxPacketFrameCount;
            if (microphoneReadPosition < 0)
            {
                microphoneReadPosition += clipFrameCount;
            }

            packetFrameCount = maxPacketFrameCount;
        }

        int outputFrameCount = GetResampledFrameCount(packetFrameCount, captureSampleRate, outputSampleRate);
        int sourceSampleValueCount = packetFrameCount * sourceChannels;
        int outputSampleValueCount = outputFrameCount * outputChannels;
        EnsureAudioBuffers(sourceSampleValueCount, outputSampleValueCount);
        ReadMicrophoneSamples(packetFrameCount, sourceChannels, clipFrameCount);
        MeasureFloatSamples(audioFloatBuffer, sourceSampleValueCount, out float inputRms, out float inputPeak);
        ConvertAudioForNetwork(packetFrameCount, sourceChannels, captureSampleRate, outputFrameCount, outputChannels, outputSampleRate);
        microphoneReadPosition = (microphoneReadPosition + packetFrameCount) % clipFrameCount;
        AdvanceNextAudioPacketTime(packetRate);

        int byteCount = EncodePcm16(outputSampleValueCount);
        AudioFrame audioFrame = new AudioFrame();
        audioFrame.sequence = audioSequence;
        audioSequence++;
        audioFrame.sampleRate = outputSampleRate;
        audioFrame.channels = outputChannels;
        audioFrame.sampleCount = outputFrameCount;
        audioFrame.encoding = "PCM16_LE_BASE64";
        audioFrame.timestamp = Time.unscaledTime;
        audioFrame.samplesBase64 = Convert.ToBase64String(audioByteBuffer, 0, byteCount);
        WriteOutgoingAudioWav(audioFrame.sampleRate, audioFrame.channels, audioByteBuffer, byteCount);
        sentAudioFrameCount++;
        WriteAudioDiagnosticsRow("sent", audioFrame.sequence, audioFrame.sampleRate, audioFrame.channels, audioFrame.sampleCount, byteCount, inputRms, inputPeak);
        LogAudioStatus("sent audio frame count=" + sentAudioFrameCount +
                       ", sequence=" + audioFrame.sequence +
                       ", sampleRate=" + audioFrame.sampleRate +
                       ", captureSampleRate=" + captureSampleRate +
                       ", channels=" + audioFrame.channels +
                       ", sampleCount=" + audioFrame.sampleCount +
                       ", bytes=" + byteCount +
                       ", inputRms=" + inputRms.ToString("F4") +
                       ", inputPeak=" + inputPeak.ToString("F4"));
        return audioFrame;
    }

    private int GetAvailableMicrophoneFrames(int microphonePosition, int clipFrameCount)
    {
        if (microphonePosition >= microphoneReadPosition)
        {
            return microphonePosition - microphoneReadPosition;
        }

        return clipFrameCount - microphoneReadPosition + microphonePosition;
    }

    private void EnsureAudioBuffers(int sourceSampleValueCount, int outputSampleValueCount)
    {
        if (audioFloatBuffer == null || audioFloatBuffer.Length != sourceSampleValueCount)
        {
            audioFloatBuffer = new float[sourceSampleValueCount];
        }

        if (audioOutputFloatBuffer == null || audioOutputFloatBuffer.Length != outputSampleValueCount)
        {
            audioOutputFloatBuffer = new float[outputSampleValueCount];
        }

        int byteCount = outputSampleValueCount * 2;
        if (audioByteBuffer == null || audioByteBuffer.Length != byteCount)
        {
            audioByteBuffer = new byte[byteCount];
        }
    }

    private void ReadMicrophoneSamples(int frameCount, int channels, int clipFrameCount)
    {
        if (microphoneReadPosition + frameCount <= clipFrameCount)
        {
            microphoneClip.GetData(audioFloatBuffer, microphoneReadPosition);
            return;
        }

        int firstFrameCount = clipFrameCount - microphoneReadPosition;
        int firstValueCount = firstFrameCount * channels;
        float[] firstBuffer = new float[firstValueCount];
        microphoneClip.GetData(firstBuffer, microphoneReadPosition);
        Array.Copy(firstBuffer, 0, audioFloatBuffer, 0, firstValueCount);

        int secondValueCount = audioFloatBuffer.Length - firstValueCount;
        if (secondValueCount <= 0)
        {
            return;
        }

        float[] secondBuffer = new float[secondValueCount];
        microphoneClip.GetData(secondBuffer, 0);
        Array.Copy(secondBuffer, 0, audioFloatBuffer, firstValueCount, secondValueCount);
    }

