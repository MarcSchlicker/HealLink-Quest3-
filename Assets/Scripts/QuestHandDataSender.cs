    private void ConvertAudioForNetwork(
        int sourceFrameCount,
        int sourceChannels,
        int sourceSampleRate,
        int outputFrameCount,
        int outputChannels,
        int outputSampleRate)
    {
        if (audioFloatBuffer == null || audioOutputFloatBuffer == null || sourceFrameCount <= 0 || outputFrameCount <= 0)
        {
            return;
        }

        sourceChannels = Mathf.Max(1, sourceChannels);
        outputChannels = Mathf.Max(1, outputChannels);

        float sourceFramesPerOutputFrame = outputSampleRate > 0
            ? (float)sourceSampleRate / outputSampleRate
            : 1f;

        for (int outputFrame = 0; outputFrame < outputFrameCount; outputFrame++)
        {
            float sourceFrameFloat = outputFrame * sourceFramesPerOutputFrame;
            int sourceFrame0 = Mathf.Clamp(Mathf.FloorToInt(sourceFrameFloat), 0, sourceFrameCount - 1);
            int sourceFrame1 = Mathf.Min(sourceFrame0 + 1, sourceFrameCount - 1);
            float blend = Mathf.Clamp01(sourceFrameFloat - sourceFrame0);

            for (int outputChannel = 0; outputChannel < outputChannels; outputChannel++)
            {
                float sample = outputChannels == 1
                    ? ReadMixedSourceFrame(sourceFrame0, sourceFrame1, blend, sourceChannels)
                    : ReadInterpolatedSourceSample(sourceFrame0, sourceFrame1, blend, Mathf.Min(outputChannel, sourceChannels - 1), sourceChannels);

                audioOutputFloatBuffer[outputFrame * outputChannels + outputChannel] = sample;
            }
        }
    }

    private float ReadMixedSourceFrame(int sourceFrame0, int sourceFrame1, float blend, int sourceChannels)
    {
        float sum = 0f;
        for (int channel = 0; channel < sourceChannels; channel++)
        {
            sum += ReadInterpolatedSourceSample(sourceFrame0, sourceFrame1, blend, channel, sourceChannels);
        }

        return sum / sourceChannels;
    }

    private float ReadInterpolatedSourceSample(int sourceFrame0, int sourceFrame1, float blend, int channel, int sourceChannels)
    {
        int index0 = sourceFrame0 * sourceChannels + channel;
        int index1 = sourceFrame1 * sourceChannels + channel;
        float sample0 = index0 >= 0 && index0 < audioFloatBuffer.Length ? audioFloatBuffer[index0] : 0f;
        float sample1 = index1 >= 0 && index1 < audioFloatBuffer.Length ? audioFloatBuffer[index1] : sample0;
        return Mathf.Lerp(sample0, sample1, blend);
    }

    private void AdvanceNextAudioPacketTime(int packetRate)
    {
        float interval = 1f / Mathf.Max(1, packetRate);
        if (nextAudioPacketTime <= 0f || Time.unscaledTime - nextAudioPacketTime > interval * Mathf.Max(1, maximumAudioPacketsPerUpdate))
        {
            nextAudioPacketTime = Time.unscaledTime + interval;
            return;
        }

        nextAudioPacketTime += interval;
    }

    private int EncodePcm16(int sampleValueCount)
    {
        float gain = Mathf.Max(0f, audioGain);
        float ceiling = Mathf.Clamp(audioLimiterCeiling, 0.1f, 1f);
        for (int i = 0; i < sampleValueCount; i++)
        {
            float sample = ApplySoftLimiter(audioOutputFloatBuffer[i] * gain, ceiling);
            short pcm = sample < 0f
                ? (short)Mathf.RoundToInt(sample * 32768f)
                : (short)Mathf.RoundToInt(sample * 32767f);
            int byteIndex = i * 2;
            audioByteBuffer[byteIndex] = (byte)(pcm & 0xff);
            audioByteBuffer[byteIndex + 1] = (byte)((pcm >> 8) & 0xff);
        }

        return sampleValueCount * 2;
    }

    private void EnsureMicrophoneCapture()
    {
        if (!sendQuestMicrophoneAudio || microphoneStartRequested || microphonePermissionDenied || microphoneStartFailed)
        {
