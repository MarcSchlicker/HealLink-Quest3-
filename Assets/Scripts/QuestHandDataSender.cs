    private bool TryGetFingerTipPositions(OVRSkeleton skeleton, out Vector3 thumbTip, out Vector3 indexTip)
    {
        thumbTip = Vector3.zero;
        indexTip = Vector3.zero;

        if (skeleton == null || skeleton.Bones == null)
        {
            return false;
        }

        bool foundThumb = false;
        bool foundIndex = false;
        for (int i = 0; i < skeleton.Bones.Count; i++)
        {
            OVRBone bone = skeleton.Bones[i];
            if (bone == null || bone.Transform == null)
            {
                continue;
            }

            string boneName = bone.Id.ToString().ToLowerInvariant();
            if (!IsTipBoneName(boneName))
            {
                continue;
            }

            if (!foundThumb && boneName.Contains("thumb"))
            {
                thumbTip = bone.Transform.position;
                foundThumb = true;
            }
            else if (!foundIndex && (boneName.Contains("index") || boneName.Contains("pointer")))
            {
                indexTip = bone.Transform.position;
                foundIndex = true;
            }

            if (foundThumb && foundIndex)
            {
                return true;
            }
        }

        return false;
    }

    private static bool TryGetBoneWorldPosition(OVRSkeleton skeleton, OVRSkeleton.BoneId boneId, out Vector3 worldPosition)
    {
        worldPosition = Vector3.zero;
        if (skeleton == null || skeleton.Bones == null)
        {
            return false;
        }

        for (int i = 0; i < skeleton.Bones.Count; i++)
        {
            OVRBone bone = skeleton.Bones[i];
            if (bone != null && bone.Id == boneId && bone.Transform != null)
            {
                worldPosition = bone.Transform.position;
                return IsFinite(worldPosition);
            }
        }

        return false;
    }

    private static bool IsTipBoneName(string boneName)
    {
        return !string.IsNullOrEmpty(boneName) &&
               (boneName.Contains("tip") || boneName.Contains("end"));
    }

