    private IEnumerator StartMicrophoneWhenAuthorized()
    {
        microphoneStartRequested = true;

        if (!Application.HasUserAuthorization(UserAuthorization.Microphone))
        {
            yield return Application.RequestUserAuthorization(UserAuthorization.Microphone);
        }

        if (!isSending || !sendQuestMicrophoneAudio)
        {
            microphoneStartRequested = false;
            yield break;
        }

        if (!Application.HasUserAuthorization(UserAuthorization.Microphone))
        {
            Debug.LogWarning("QuestHandDataSender cannot send microphone audio because microphone permission was not granted.");
            microphonePermissionDenied = true;
            microphoneStartRequested = false;
            yield break;
        }

        string deviceName = TrimToNull(microphoneDeviceName);
        string[] devices = Microphone.devices;
        if (logAudioStatus)
        {
            Debug.Log("QuestHandDataSender microphone devices: " + (devices == null || devices.Length == 0 ? "(none)" : string.Join(", ", devices)));
        }

        if (!string.IsNullOrEmpty(deviceName) && !IsMicrophoneDeviceAvailable(deviceName))
        {
            Debug.LogWarning("QuestHandDataSender microphone device '" + deviceName + "' was not found. Falling back to the default microphone.");
            deviceName = null;
        }

        int requestedSampleRate = Mathf.Clamp(audioSampleRate, 8000, 48000);
        try
        {
            microphoneClip = Microphone.Start(deviceName, true, 10, requestedSampleRate);
            activeMicrophoneDeviceName = deviceName;
            microphoneReadPosition = 0;
            audioSequence = 0;
            nextAudioPacketTime = 0f;

            if (microphoneClip == null)
            {
                microphoneStartFailed = true;
                Debug.LogWarning("QuestHandDataSender could not start microphone capture because Microphone.Start returned no clip.");
            }
            else if (logAudioStatus)
            {
                Debug.Log("QuestHandDataSender microphone capture started, device=" + (activeMicrophoneDeviceName ?? "(default)") +
                          ", frequency=" + microphoneClip.frequency +
                          ", channels=" + microphoneClip.channels +
                          ", samples=" + microphoneClip.samples);
            }

            if (microphoneClip != null)
            {
                WriteAudioDiagnosticsEvent("microphone_started", microphoneClip.frequency, microphoneClip.channels, microphoneClip.samples);
            }
        }
        catch (Exception e)
        {
            microphoneClip = null;
            activeMicrophoneDeviceName = null;
            microphoneStartFailed = true;
            Debug.LogWarning("QuestHandDataSender could not start microphone capture: " + e.Message);
        }

        microphoneStartRequested = false;
    }

    private void StopMicrophoneCapture()
    {
        if (microphoneClip != null || microphoneStartRequested)
        {
            try
            {
                if (Microphone.IsRecording(activeMicrophoneDeviceName))
