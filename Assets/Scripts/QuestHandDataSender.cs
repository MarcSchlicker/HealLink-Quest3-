                }
            }
            catch (Exception e)
            {
                Debug.LogWarning("QuestHandDataSender could not stop microphone capture cleanly: " + e.Message);
            }
        }

        microphoneClip = null;
        activeMicrophoneDeviceName = null;
        microphoneReadPosition = 0;
        microphoneStartRequested = false;
        earliestMicrophoneStartTime = 0f;
    }

