    [Serializable]
    private struct PoseData
    {
        public bool isValid;
        public Vector3 position;
        public Quaternion rotation;

        public static readonly PoseData Invalid = new PoseData
        {
            isValid = false,
            position = Vector3.zero,
            rotation = Quaternion.identity
        };

        public static PoseData FromTransform(
            Transform source,
            Transform reference,
            float scale,
            Vector3 worldPositionOffset)
        {
            if (source == null)
            {
                return Invalid;
            }

            Vector3 worldPosition = source.position + worldPositionOffset;
            if (reference == null)
            {
                return new PoseData
                {
                    isValid = true,
                    position = worldPosition * scale,
                    rotation = source.rotation
                };
            }

            return new PoseData
            {
                isValid = true,
                position = reference.InverseTransformPoint(worldPosition) * scale,
                rotation = Quaternion.Inverse(reference.rotation) * source.rotation
            };
        }

        public static PoseData FromPosition(
            Vector3 worldPosition,
            Transform reference,
            float scale,
            Vector3 worldPositionOffset)
        {
            Vector3 shiftedWorldPosition = worldPosition + worldPositionOffset;
            if (reference == null)
            {
                return new PoseData
                {
                    isValid = true,
                    position = shiftedWorldPosition * scale,
                    rotation = Quaternion.identity
                };
            }

            return new PoseData
            {
                isValid = true,
                position = reference.InverseTransformPoint(shiftedWorldPosition) * scale,
                rotation = Quaternion.identity
            };
        }
    }
