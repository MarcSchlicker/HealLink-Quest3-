    private void UpdateLeftDepthHandPreview()
    {
        if (!showLeftHandAtDepthWhilePinching ||
            !hasCurrentPinchDepthPoint ||
            leftSkeleton == null ||
            !TryGetFingerTipPositions(leftSkeleton, out Vector3 thumbTip, out Vector3 indexTip))
        {
            HideLeftDepthHandPreview();
            return;
        }

        EnsureLeftDepthHandPreview();
        if (leftDepthPreviewRoot == null || leftDepthPreviewClone == null)
        {
            return;
        }

        Vector3 pinchMidpoint = (thumbTip + indexTip) * 0.5f;
        Vector3 targetOffset = currentPinchDepthPoint - pinchMidpoint;
        if (!IsFinite(targetOffset))
        {
            HideLeftDepthHandPreview();
            return;
        }

        if (!hasSmoothedLeftDepthPreviewOffset)
        {
            hasSmoothedLeftDepthPreviewOffset = true;
            smoothedLeftDepthPreviewOffset = Vector3.zero;
            leftDepthPreviewOffsetVelocity = Vector3.zero;
        }

        smoothedLeftDepthPreviewOffset = Vector3.SmoothDamp(
            smoothedLeftDepthPreviewOffset,
            targetOffset,
            ref leftDepthPreviewOffsetVelocity,
            Mathf.Max(0.001f, pinchDepthHandSmoothingTime),
            Mathf.Max(0.01f, pinchDepthHandMaximumSpeed),
            Mathf.Max(0.0001f, Time.unscaledDeltaTime));

        Transform sourceRoot = leftSkeleton.transform;
        leftDepthPreviewRoot.transform.SetPositionAndRotation(
            sourceRoot.position + smoothedLeftDepthPreviewOffset,
            sourceRoot.rotation);
        leftDepthPreviewRoot.transform.localScale = sourceRoot.lossyScale;
        leftDepthPreviewClone.transform.localPosition = Vector3.zero;
        leftDepthPreviewClone.transform.localRotation = Quaternion.identity;
        leftDepthPreviewClone.transform.localScale = Vector3.one;

        for (int i = 0; i < leftDepthPreviewTransformPairs.Count; i++)
        {
            TransformPair pair = leftDepthPreviewTransformPairs[i];
            if (pair.source == null || pair.preview == null)
            {
                continue;
            }

            pair.preview.localPosition = pair.source.localPosition;
            pair.preview.localRotation = pair.source.localRotation;
            pair.preview.localScale = pair.source.localScale;
        }

        SetLeftDepthHandPreviewVisible(true);
    }

    private void EnsureLeftDepthHandPreview()
    {
        if (leftDepthPreviewRoot != null || leftSkeleton == null)
        {
            return;
        }

        Transform sourceRoot = leftSkeleton.transform;
        leftDepthPreviewRoot = new GameObject("QuestLeftHandDepthPreviewRoot");
        leftDepthPreviewRoot.SetActive(false);
        leftDepthPreviewRoot.transform.SetPositionAndRotation(sourceRoot.position, sourceRoot.rotation);
        leftDepthPreviewRoot.transform.localScale = sourceRoot.lossyScale;

        leftDepthPreviewClone = Instantiate(sourceRoot.gameObject, leftDepthPreviewRoot.transform, false);
        leftDepthPreviewClone.name = "QuestLeftHandDepthPreviewVisual";
        leftDepthPreviewClone.transform.localPosition = Vector3.zero;
        leftDepthPreviewClone.transform.localRotation = Quaternion.identity;
        leftDepthPreviewClone.transform.localScale = Vector3.one;

        MonoBehaviour[] clonedBehaviours = leftDepthPreviewClone.GetComponentsInChildren<MonoBehaviour>(true);
        for (int i = 0; i < clonedBehaviours.Length; i++)
        {
            clonedBehaviours[i].enabled = false;
        }

        Collider[] clonedColliders = leftDepthPreviewClone.GetComponentsInChildren<Collider>(true);
        for (int i = 0; i < clonedColliders.Length; i++)
        {
            clonedColliders[i].enabled = false;
        }

        leftDepthPreviewTransformPairs.Clear();
        BuildLeftDepthPreviewTransformPairs(sourceRoot, leftDepthPreviewClone.transform);

        originalLeftRendererStates.Clear();
        Renderer[] sourceRenderers = sourceRoot.GetComponentsInChildren<Renderer>(true);
        for (int i = 0; i < sourceRenderers.Length; i++)
        {
            originalLeftRendererStates.Add(new RendererState
            {
                renderer = sourceRenderers[i],
                enabled = sourceRenderers[i].enabled
            });
        }

        leftDepthPreviewRenderers.Clear();
        leftDepthPreviewRenderers.AddRange(
            leftDepthPreviewClone.GetComponentsInChildren<Renderer>(true));
        for (int i = 0; i < leftDepthPreviewRenderers.Count; i++)
        {
            if (leftDepthPreviewRenderers[i] != null)
            {
                leftDepthPreviewRenderers[i].enabled = false;
            }
        }

        leftDepthPreviewVisible = false;
        leftDepthPreviewRoot.SetActive(true);
    }

    private void BuildLeftDepthPreviewTransformPairs(Transform source, Transform preview)
    {
        int childCount = Mathf.Min(source.childCount, preview.childCount);
        for (int i = 0; i < childCount; i++)
        {
            Transform sourceChild = source.GetChild(i);
            Transform previewChild = preview.GetChild(i);
            leftDepthPreviewTransformPairs.Add(new TransformPair
            {
                source = sourceChild,
                preview = previewChild
            });
            BuildLeftDepthPreviewTransformPairs(sourceChild, previewChild);
        }
    }

    private void SetLeftDepthHandPreviewVisible(bool visible)
    {
        if (leftDepthPreviewVisible == visible)
        {
            return;
        }

        leftDepthPreviewVisible = visible;
        for (int i = 0; i < originalLeftRendererStates.Count; i++)
        {
            RendererState state = originalLeftRendererStates[i];
            if (state.renderer == null)
            {
                continue;
            }

            if (visible)
            {
                state.enabled = state.renderer.enabled;
                state.renderer.enabled = false;
            }
            else
            {
                state.renderer.enabled = state.enabled;
            }
        }

        for (int i = 0; i < leftDepthPreviewRenderers.Count; i++)
        {
            Renderer renderer = leftDepthPreviewRenderers[i];
            if (renderer != null)
            {
                bool sourceWasVisible =
                    i >= originalLeftRendererStates.Count ||
                    originalLeftRendererStates[i].enabled;
                renderer.enabled = visible && sourceWasVisible;
            }
        }
    }

