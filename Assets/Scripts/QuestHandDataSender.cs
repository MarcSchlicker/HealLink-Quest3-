/// <summary>
/// Sends Quest hand poses, left-hand pinch drawing strokes, and optional Quest microphone audio to the HoloLens.
/// Receiver contract:
/// - poseSpace "CameraLocal" means points were stored relative to the Quest reference camera. The HoloLens receiver
///   should convert each stroke point through its own reference camera into HoloLens world space once, then leave it there.
/// - poseSpace "World" means stroke points can be used directly as world positions.
/// - strokeEvents contain "begin", "point", and "end" actions. "begin" creates a persistent red LineRenderer, "point"
///   appends 3D points, and "end" finalizes the line without destroying it on the HoloLens.
/// - when projectStrokeOntoDepth is enabled, stroke points are already projected from the Quest reference camera through
///   the pinch midpoint onto the measured depth image. The receiver should place the red line at those 3D points directly,
///   without attaching it to the billboard.
/// - audioFrame contains continuous microphone audio as PCM16 little-endian Base64 data. Decode it, enqueue it by sequence,
///   and feed it into a streaming AudioSource. Audio is independent from the pinch gesture.
/// Quest-local stroke previews are temporary only on the Quest and can be destroyed after questStrokeLifetimeSeconds.
/// </summary>
