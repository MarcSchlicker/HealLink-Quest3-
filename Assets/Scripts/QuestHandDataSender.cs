    private void AppendLeftPinchStrokePoint(Vector3 worldPoint)
    {
        if (string.IsNullOrEmpty(activeStrokeId))
        {
            BeginLeftPinchStroke(worldPoint);
            return;
        }

        if (currentQuestStrokePoints.Count > 0)
        {
            Vector3 previousPoint = currentQuestStrokePoints[currentQuestStrokePoints.Count - 1];
            if (Vector3.Distance(previousPoint, worldPoint) < strokePointSpacing)
            {
                return;
            }
        }

        currentQuestStrokePoints.Add(worldPoint);
        QueueStrokeEvent("point", worldPoint);
        UpdateQuestStrokePreview();
    }

    private void EndActiveLeftPinchStroke()
    {
        if (!leftPinchWasActive && string.IsNullOrEmpty(activeStrokeId))
        {
            return;
        }

        if (!string.IsNullOrEmpty(activeStrokeId) && currentQuestStrokePoints.Count > 0)
        {
            Vector3 lastPoint = currentQuestStrokePoints[currentQuestStrokePoints.Count - 1];
            QueueStrokeEvent("end", lastPoint);
        }

        FinishActiveStrokePreview();
        leftPinchWasActive = false;
        hasCurrentPinchDepthPoint = false;
        hasLastDepthProjectedStrokePoint = false;
        lastDepthProjectedStrokePoint = Vector3.zero;
        hasLastDepthProjectedForwardDistance = false;
        lastDepthProjectedForwardDistance = 0f;
        activeStrokeId = null;
        currentQuestStrokePoints.Clear();
    }

