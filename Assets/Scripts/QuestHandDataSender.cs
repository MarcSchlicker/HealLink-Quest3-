    private Vector3 GetQuestStrokePreviewPoint(Vector3 worldPoint)
    {
        if (!keepQuestStrokePreviewVisible)
        {
            return worldPoint;
        }

        Transform cameraTransform = depthProjectionCamera != null ? depthProjectionCamera : referenceCamera;
        if (cameraTransform == null)
        {
            Camera mainCamera = Camera.main;
            cameraTransform = mainCamera != null ? mainCamera.transform : null;
        }

        if (cameraTransform == null)
        {
            return worldPoint;
        }

        Vector3 cameraToPoint = worldPoint - cameraTransform.position;
        float distance = cameraToPoint.magnitude;
        if (distance <= 0.001f)
        {
            return worldPoint;
        }

        float previewDistance = Mathf.Max(0.05f, distance - questStrokePreviewCameraOffset);
        return cameraTransform.position + cameraToPoint / distance * previewDistance;
    }

    private Vector3 GetPreviewFallbackDirection()
    {
        if (referenceCamera != null)
        {
            return referenceCamera.right.normalized;
        }

        Camera mainCamera = Camera.main;
        if (mainCamera != null)
        {
            return mainCamera.transform.right.normalized;
        }

        return Vector3.right;
    }

